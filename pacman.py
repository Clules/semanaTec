# Seleccionar un tablero aleatorio
tiles = choice(board_templates)

def square(x, y):
    """Dibuja un cuadrado en la posición (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()
    for count in range(4):
        path.forward(20)
        path.left(90)
    path.end_fill()

def offset(point):
    """Devuelve el índice de un punto en la lista de tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Verifica si un punto es válido (no es una pared)."""
    index = offset(point)
    if tiles[index] == 0:
        return False
    index = offset(point + 19)
    if tiles[index] == 0:
        return False
    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Dibuja el mundo utilizando el tablero definido en tiles."""
    bgcolor('black')
    path.color('blue')
    for index in range(len(tiles)):
        tile = tiles[index]
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)
            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move_pacman():
    """Mueve a Pacman."""
    if valid(pacman + aim):
        pacman.move(aim)
    index = offset(pacman)
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

def move_ghosts():
    """Mueve a todos los fantasmas y verifica si tocan a Pacman."""
    for point, course in ghosts:
        distance = abs(pacman - point)
        options = [
            vector(5, 0),
            vector(-5, 0),
            vector(0, 5),
            vector(0, -5),
        ]
        if distance < 100:
            best_option = min(options, key=lambda option: abs((point + option) - pacman))
            if valid(point + best_option):
                course.x = best_option.x
                course.y = best_option.y
        else:
            plan = choice(options)
            if valid(point + plan):
                course.x = plan.x
                course.y = plan.y
        if valid(point + course):
            point.move(course)
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

        # Verificar colisión con Pacman
        if abs(pacman - point) < 20:
            print("¡Pacman fue comido!")
            return  # Termina el juego

    update()
    ontimer(move_ghosts, 50)  # Actualiza la velocidad de los fantasmas

def move():
    """Mueve Pacman y actualiza la puntuación."""
    writer.undo()
    writer.write(state['score'])
    clear()
    move_pacman()
    ontimer(move, 100)  # Pacman se mueve a un ritmo estándar

def change(x, y):
    """Cambia la dirección de Pacman si el movimiento es válido."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

